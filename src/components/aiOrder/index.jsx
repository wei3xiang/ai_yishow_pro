import React, { useState } from "react";
import data from "../data/ai_order.json";

import List from "./List";
import UpdateForm from "./UpdateForm";
import DisableForm from "./DisableForm";
import RemoveForm from "./RemoveForm";
import CreateForm from "./CreateForm";

const Index = () => {
  const [list, setList] = useState(data);

  const handleEdit = (aiOrder) => {
    const modalContent = `${UpdateForm({ aiOrder: aiOrder })}`;
    layui.layer.open({
      type: 1,
      title: "编辑",
      area: ["500px", "350px"],
      content: modalContent,
      success: function (layero, index) {
        layero.find("#modalCancel").on("click", () => layui.layer.close(index));
        layero.find("#modalOk").on("click", () => {
          const updatedItem = {
            ...aiOrder,
              aiOrderId: layero.find('input[name="aiOrderId"]').val(), 
              aiUserId: layero.find('input[name="aiUserId"]').val(), 
              aiProductId: layero.find('input[name="aiProductId"]').val(), 
              aiQuantity: layero.find('input[name="aiQuantity"]').val(), 
              aiOrderTime: layero.find('input[name="aiOrderTime"]').val(), 
              aiCategory: layero.find('input[name="aiCategory"]').val(), 
          };
          setList(
            list.map((u) => (u.uuid === updatedItem.uuid ? updatedItem : u))
          );
          layui.layer.close(index);
        });
      },
    });
  };

  const handleDetail = (aiOrder) => {
    const modalContent = `${DisableForm({ aiOrder: aiOrder })}`;
    layui.layer.open({
      type: 1,
      title: "查看",
      area: ["500px", "350px"],
      content: modalContent,
      success: function (layero, index) {
        layero.find("#modalCancel").on("click", () => layui.layer.close(index));
      },
    });
  };

  const handleRemove = (aiOrder) => {
    const modalContent = `${RemoveForm({ aiOrder: aiOrder })}`;

    layui.layer.open({
      type: 1,
      title: "确认删除",
      area: ["500px", "350px"],
      content: modalContent,
      success: function (layero, index) {
        layero.find("#modalCancel").on("click", () => layui.layer.close(index));
        layero.find("#modalOk").on("click", () => {
          setList(list.filter((u) => u.uuid !== aiOrder.uuid));
          layui.layer.close(index);
        });
      },
    });
  };

  const handleCreate = () => {
    const newItem = { uuid: "",  aiOrderId: "",  aiUserId: "",  aiProductId: "",  aiQuantity: "",  aiOrderTime: "",  aiCategory: "", };
    const modalContent = `${CreateForm()}`;

    layui.layer.open({
      type: 1,
      title: "新增",
      area: ["500px", "350px"],
      content: modalContent,
      success: function (layero, index) {
        layero.find("#modalCancel").on("click", () => layui.layer.close(index));
        layero.find("#modalOk").on("click", () => {

          const aiOrderId = layero.find('input[name="aiOrderId"]').val();
          const aiUserId = layero.find('input[name="aiUserId"]').val();
          const aiProductId = layero.find('input[name="aiProductId"]').val();
          const aiQuantity = layero.find('input[name="aiQuantity"]').val();
          const aiOrderTime = layero.find('input[name="aiOrderTime"]').val();
          const aiCategory = layero.find('input[name="aiCategory"]').val();
           if (
           aiOrderId &&

           aiUserId &&

           aiProductId &&

           aiQuantity &&

           aiOrderTime &&

           aiCategory
           ) {
            setList([...list, { ...newItem, uuid: Date.now(), aiOrderId,
          aiUserId,
          aiProductId,
          aiQuantity,
          aiOrderTime,
          aiCategory,
           }]);
            layui.layer.close(index);
          } else {
            layui.layer.msg("请填写完整信息", { icon: 5 });
          }
        });
      },
    });
  };

  return (
    <div style={{ padding: "20px" }}>
      <List
        data = {list}
        onClickCreate={() => handleCreate()}
        onClickRemove={(user) => handleRemove(user)}
        onClickDetail={(user) => handleDetail(user)}
        onClickUpdate={(user) => handleEdit(user)}
      />
    </div>
  );
};

export default Index;
